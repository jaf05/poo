    public void listar() {
        dtmhistorial.setNumRows(0);
        for (int i = 0; i < histcontroller.size(); i++) {
            Object[] vec = new Object[10];
            vec[0] = histcontroller.get(i).getHistclini();
            vec[1] = histcontroller.get(i).getCita();
            vec[2] = histcontroller.get(i).getSintomas();
            vec[3] = histcontroller.get(i).getTiempos();
            vec[4] = histcontroller.get(i).getEnfermedad();
            vec[5] = histcontroller.get(i).getTemperatura();
            vec[6] = histcontroller.get(i).getFrecuenciacardi();
            vec[7] = histcontroller.get(i).getFrecuenciarespi();
            vec[8] = histcontroller.get(i).getSaturacion();
            vec[9] = histcontroller.get(i).getAnamnesis();
            dtmhistorial.addRow(vec);
        }
        tHistorial.setModel(dtmhistorial);
    }
    
