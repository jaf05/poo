    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        int msg = JOptionPane.showConfirmDialog(this, "Deseas Registrar la información", "Registrar", JOptionPane.YES_NO_OPTION);
        if (msg == JOptionPane.YES_OPTION) {

            Historialc hist = new Historialc() {
            };
            hist.setHistclini(Integer.parseInt(this.txtc.getText()));
            hist.setCita(Integer.parseInt(this.txtc.getText()));
            hist.setSintomas(this.txts.getText());
            hist.setTiempos(Integer.parseInt(this.txtt.getText()));
            hist.setEnfermedad(this.txte.getText());
            hist.setTemperatura(Double.parseDouble(this.txttem.getText()));
            hist.setFrecuenciacardi(Integer.parseInt(this.txtfc.getText()));
            hist.setFrecuenciarespi(Integer.parseInt(this.txtfr.getText()));
            hist.setSaturacion(Integer.parseInt(this.txtsa.getText()));
            hist.setAnamnesis(this.txta.getText());

            //Insertar el objeto al arreglo
            histcontroller.add(hist);

            histcontroller.grabar(); //METODO DE GRABAR en controller "semana 11"
            listar();

            JOptionPane.showMessageDialog(this, "Datos registrados satisfactoriamente!!!");
            bloquear_controles();
            btneditar.setEnabled(true);  //habilitad el boton editar
        } else {
            txtc.requestFocus();
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        int msg = JOptionPane.showConfirmDialog(this, "Deseas modificar el registro", "Modificar", JOptionPane.YES_NO_OPTION);
        if (msg == JOptionPane.YES_OPTION) {
            Historialc hist = histcontroller.buscar(Integer.parseInt(txth.getText()));
            if (hist != null) {  //si pro es diferente de null

                //Modificadores
                hist.setCita(Integer.parseInt(this.txtc.getText()));
                hist.setSintomas(this.txts.getText());
                hist.setTiempos(Integer.parseInt(this.txtt.getText()));
                hist.setEnfermedad(this.txte.getText());
                hist.setTemperatura(Double.parseDouble(this.txttem.getText()));
                hist.setFrecuenciacardi(Integer.parseInt(this.txtfc.getText()));
                hist.setFrecuenciarespi(Integer.parseInt(this.txtfr.getText()));
                hist.setSaturacion(Integer.parseInt(this.txtsa.getText()));
                hist.setAnamnesis(this.txta.getText());

                //para actualizar se llama al metodo listar
                listar();

                histcontroller.grabar();
                JOptionPane.showMessageDialog(this, "Registro actualizado satisfactoriamente");

            } else {
                JOptionPane.showMessageDialog(this, "Objeto no encontrado");
            }
        }


    }//GEN-LAST:event_btneditarActionPerformed

